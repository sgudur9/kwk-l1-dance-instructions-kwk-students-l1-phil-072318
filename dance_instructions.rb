end

def lasso
  puts "Start arm movement by holding left arm so the back of your wrist is near your chin, pointing your left elbow straight left"
  puts "Lift right arm up and point right elbow diagonally to the right"
  puts "Raise right forearm so that it points straight up, and whip it in small circles to the beat of the song, as if you were a cowboy holding a rope lasso. Do this eight times as well"
end

